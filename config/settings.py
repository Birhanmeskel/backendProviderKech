def _ensure_scheme(origin: str) -> str:
    """Add http:// or https:// scheme if missing."""
    origin = origin.strip()
    if not origin:
        return origin
    if origin.startswith(("http://", "https://")):
        return origin
    return f"https://{origin}" if not DEBUG else f"http://{origin}"

